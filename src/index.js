module.exports = {
    sum: (a, b) => {
        return a + b;
    },
    minus: (a, b) => {
        return a - b;
    },
    multi: (a, b) => {
        return a * b;
    },
    pow: (a, b) => {
        return a ** b;
    },
    max: (a, b) => {
        return a > b ? a : b;
    },
    min: (a, b) => {
        return a < b ? a : b;
    }
}